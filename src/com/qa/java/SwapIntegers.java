package com.qa.java;

public class SwapIntegers {

	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		a=a+b;
		b=a-b;
		a=a-b;
		System.out.println(a);
		System.out.println(b);
		int n1=20;
		int n2=30;
		int temp;
		temp=n1;
		n1=n2;
		n2=temp;
		System.out.println(n1);
		System.out.println(n2);
		
	}

}
